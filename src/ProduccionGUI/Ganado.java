    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (Raza.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese todos  los atributos necesarios ", " informacion", JOptionPane.INFORMATION_MESSAGE);

            Raza.setText("");

            Raza.setText("");

        } else {
            int madre = 0;
            if (num(Raza.getText()) && alfnumesp(Raza.getText()) && counter(Raza.getText()) < 30) {
                if (Raza.getText().equals("")) {
                    madre = 0;
                } else {
                    madre = Integer.parseInt(Raza.getText());
                }
                try {
                    ProcedimientoGanado.ingresoGanado(madre, Sexo.getSelectedItem().toString(), Raza.getText(), Salud.getSelectedItem().toString());

                    Raza.setText("");

                    Raza.setText("");

                    Raza.requestFocus();
                    Raza.requestFocus();

                    JOptionPane.showMessageDialog(this, " Los datos  han sido ingresados  correctamente ");
                } catch (SQLException ex) {
                    Logger.getLogger(Ganado.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(this, " Error en el ingreso de datos");
            }

        }
        // TODO add your handling code here:
    }

    public static int counter(String str) {
        int count = 0;
        for (int i = 0; i < str.length(); i++) {
            if (str.charAt(i) != ' ') {
                count++;
            }

        }
        return count;
    }

    //Regex Alfanumerico + Espacio
    public static boolean alfnumesp(String str) {

        if (Pattern.matches("^[a-zA-Z ]*", str)) {
            return true;
        } else {
            return false;
        }

    }

    //Regex Numerico
    public static boolean num(String str) {

        if (Pattern.matches("^[0-9]*", str)) {
            return true;
        } else {
            return false;
        }

    }

    //Regex Direccion
    public static boolean direccion(String str) {

        if (Pattern.matches("^[a-zA-Z-.]*", str)) {
            return true;
        } else {
            return false;
        }

    }

